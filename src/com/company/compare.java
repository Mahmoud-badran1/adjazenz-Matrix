package com.company;

public class compare {
    public static int maximum(int small, int big) {
        if (small>big) {
            return small;
        } else {
            return big;
        }
    }

    public static int minmum(int small, int big) {
        if (small<big) {
            return small;
        } else {
            return big;
        }
    }
}
